class OperationsBasiques:
    "Classe contenant des opérations arithmétiques basiques."

    def addition(self, a, b):
        """
        Fonction arithmétique de l'addition.

        Parameters
        ----------
        a : int
            nombre quelconque.
        b : int
            nombre quelconque.

        Returns
        -------
        int
            Résultat de a + b.
        """
        return a + b

    def soustraction(self, a, b):
        """
        Fonction arithmétique de la soustraction.

        Parameters
        ----------
        a : int
            nombre quelconque.
        b : int
            nombre quelconque.

        Returns
        -------
        int
            Résultat de a - b.
        """
        return a - b

    def multiplication(self, a, b):
        """
        Fonction arithmétique de la multiplication.

        Parameters
        ----------
        a : int
            nombre quelconque.
        b : int
            nombre quelconque.

        Returns
        -------
        int
            Résultat de a x b.
        """
        return a * b

    def division(self, a, b):
        """
        Fonction arithmétique de la division.

        Parameters
        ----------
        a : int
            nombre quelconque.
        b : int
            nombre quelconque.

        Returns
        -------
        int
            Résultat de a / b.
        """
        return a / b
